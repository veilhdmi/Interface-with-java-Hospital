    public void limpiarTabla() {
        for (int i = modeloTablaCita.getRowCount() - 1; i >= 0; i--) {
            modeloTablaCita.removeRow(i);
        }
    }

    public void actualizarTablaCita() {
        limpiarTabla();
        // Recuperar los datos de Gestion Persona
        
        for (int i = 0; i < gestorCita.longitud(); i++) {
            Cita cita = gestorCita.iesimo(i);
            String[] fila = new String[3]; // IDVenta, Fecha, Cliente, total           
            int id = cita.getNumero();
            // Convertir un entero a texto
            fila[0] = String.valueOf(id);
            fila[1] = String.valueOf(cita.getFecha());
            fila[2] = String.valueOf(cita.getCliente());
            // Agregar un registro al modelo Tabla
            modeloTablaCita.addRow(fila);
        }
        // Agragar los datos a la TablaPersona
    }


    public void limpiarTablaServicio() {
        for (int i = modeloTablaServicio.getRowCount() - 1; i >= 0; i--) {
            modeloTablaServicio.removeRow(i);
        }
    }
    /*
     * Actualizar el modelo tabla
     */

    public void actualizarTablaServicio() {
      
        limpiarTablaServicio();
     
        for (int i = 0; i < gestorServicio.longitud(); i++) {
            Servicio servicio = gestorServicio.iesimo(i);
            String[] fila = new String[2];        
            int id = servicio.getId();
           
            fila[0] = String.valueOf(id);
            fila[1] = servicio.getNombre();
           
           
            modeloTablaServicio.addRow(fila);
        }
        
    }


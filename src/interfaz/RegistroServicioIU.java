    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
      
        int id = Integer.valueOf(txtID.getText());
        String nombre = txtNombre.getText();
        
        Servicio producto = new Servicio(id, nombre);
      
        gestorServicio.agregar(producto);
       
        actualizarTablaServicio();
        JOptionPane.showMessageDialog(rootPane, "Se agrego con Exito..");
      
        txtID.setText("");
        txtNombre.setText("");
        txtID.requestFocus(); 
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        int resultado = JOptionPane.
                showConfirmDialog(rootPane, "Desea eliminar?", "Eliminar Producto", JOptionPane.YES_NO_OPTION);
        if (resultado == JOptionPane.YES_OPTION) {
           
            int numFila = tablaProducto.getSelectedRow();
            if (numFila >= 0) {
                
                Object temp = modeloTablaServicio.getValueAt(numFila, 0); 
                int id = Integer.valueOf(String.valueOf(temp));
              
                int pos = gestorServicio.posicion(id);
              
                gestorServicio.eliminar(pos);
               
                actualizarTablaServicio();
            } else { 
                JOptionPane.showMessageDialog(rootPane, "Debe seleccionar una fila");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

